export const getUserPortfolio = catchAsync(async (req, res, next) => {
  let query = User.findById(req.params.id).populate("property");

  const doc = await query;

  if (!doc) {
    return next(new AppError("No document found with that ID", 404));
  }

  res.status(200).json({
    status: "success",
    data: doc,
  });
});

